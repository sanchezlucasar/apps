  $(document).ready(function () {

    var base_url ="<?php echo base_url(); ?>";

    
     $(".btn-removes").on("click",function(e){
      e.preventDefault();
      var ruta=$(this).attr("href");
      //alert(ruta);
     
      $.ajax({
        url: ruta,
        type:"POST",
        success : function(resp){

          window.location.href = base_url + resp;
      }
    });

    }); 
         $(".btn-view").on("click",function(){

      var id = $(this).val();

      $.ajax({
       url: base_url+"index.php/categories/view/"+id,
       type:"POST",
       success : function(resp){

        $("#modal-default .modal-body").html(resp);
       // alert(resp);
      }
      });

   });

    
     $(".btn-user").on("click",function(){

      var id = $(this).val();

      $.ajax({
       url: base_url+"index.php/user/view/"+id,
       type:"POST",
       success : function(resp){

        $("#modal-default .modal-body").html(resp);
       // alert(resp);
      }
      });

   });

  $("#productos").autocomplete({
      source:function (request, response){
        $.ajax({

          url: base_url +"index.php/ventas/getproductos",
          type: "POST",
          dataType: "json",
          data:{ valor: request.term },
          success: function(data){
            response(data);
          }

        });
      },
      minLength:2,
      select: function(event, ui){
        data = ui.item.id + "*"+ ui.item.codigo+"*"+ ui.item.label+"*"+ ui.item.descripcion+"*"+ui.item.precio;
        $("#btn-agregar").val(data);
      }

    });
 
  $("#btn-agregar").on("click", function(){
    data = $(this).val();
    if (data!= ''){
      
      var infoproducto = data.split("*");
      html = "<tr>";
      html += "<td><input type='hidden' name='idproductos[]' value='"+infoproducto[1]+"'>"+infoproducto[1]+"</td>";
      html += "<td>"+infoproducto[2]+"</td>";
      html += "<td>"+infoproducto[3]+"</td>";
      html += "<td><input type='hidden' name='precios[]' value='"+infoproducto[4]+"'>"+infoproducto[4]+"</td>";
      html += "<td><button type='button' class='btn btn-danger btn-remove-producto'><span class='fa fa-remove'></span></button></td>";
      html += "<tr>";
      $("#tbventas tbody").append(html);
      sumar();

      $("#btn-agregar").val(null);
      $("#producto").val(null);

     
    }else{
      alert("seleccione un producto...");
    }
  });

  $(document).on('click', ".btn-remove-producto", function (){

    $(this).closest("tr").remove();
    sumar();

  });

    
    $(".btn-view-app").on("click",function(){

      var producto = $(this).val();

      var infoproducto = producto.split("*"); 

      html =  "<p><strong>Id: </strong>"+infoproducto[0]+"</p>"
      html += "<p><strong>Codigo: </strong>"+infoproducto[1]+"</p>"
      html += "<p><strong>Nombre: </strong>"+infoproducto[2]+"</p>"
      html += "<p><strong>Descripcion: </strong>"+infoproducto[3]+"</p>"
      html += "<p><strong>Precio: </strong>"+infoproducto[4]+"</p>"
      html += "<p><strong>Categoria: </strong>"+infoproducto[5]+"</p>";

     $("#modal-default .modal-body").html(html);

   });

    

    

    $('#example1').DataTable();
    $('.sidebar-menu').tree();
  });

  function sumar(){
    
    var total=0;
    
    $("#tbventas tbody tr").each(function(){
      
     total = total + Number($(this).find("td:eq(3)").text()); 
     });
       
      
    $("input[name=total]").val(total);
 
  }
